void	ft_sort_int_tab(int *tab, int size)
{
	int	a;
	int	i;

	i = 0;
	while (i < (size - 1))
	{
		if (tab[i] > tab[(i + 1)])
		{
			a = tab[(i + 1)];
			tab[(i + 1)] = tab[i];
			tab[i] = a;
			i = 0;
		}
		else
			i++;
	}
}
